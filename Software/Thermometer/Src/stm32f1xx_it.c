/**
  * @brief This function handles System tick timer.
  */
void SysTick_Handler(void)
{
  /* USER CODE BEGIN SysTick_IRQn 0 */
	uint32_t tick_ms;
	extern float temperature;
	
	uint16_t ADC_out;
	float ADC_volt;
	
	const float VREF = 0.652f;
	const float VMAX = 3.21f;
	
	float RNTC;
	const float B   = 3950;
	const float R25 = 10060;
	const float Rf  = 3300;
	const float T0  = 273.15f;
  
	float VS9013;
	const float Rf1 = 13.64f;
	const float Rf2 = 197.8f;
	const float ratio = Rf2 / Rf1;
  /* USER CODE END SysTick_IRQn 0 */
  HAL_IncTick();
  /* USER CODE BEGIN SysTick_IRQn 1 */
	tick_ms = HAL_GetTick();
	
  if(!(tick_ms % 100))
  {
  	HAL_ADC_Start(&hadc1);
  	HAL_ADC_PollForConversion(&hadc1, 1);
		
  	if(HAL_IS_BIT_SET(HAL_ADC_GetState(&hadc1),HAL_ADC_STATE_REG_EOC))
  	{
  	  ADC_out = HAL_ADC_GetValue(&hadc1);
			ADC_volt = ADC_out * VMAX / 4096;
			printf("ADC: %4d, %8.6f ",ADC_out, ADC_volt);
			
			if (HAL_GPIO_ReadPin(S9013_GPIO_Port,S9013_Pin)){
				VS9013 = ((1 + ratio) * VREF - ADC_volt) / ratio;
				temperature = -527.983f * VS9013 + 358.923;
				printf("S9013, %8.6f\r\n", temperature);
			}else if (HAL_GPIO_ReadPin(NTC_GPIO_Port,NTC_Pin)){
				RNTC = Rf / (ADC_volt / VREF - 1);
				temperature = 1 / (log(RNTC / R25) / B + 1 / (T0 + 24)) - T0;
				printf(" NTC , %8.6f\r\n", temperature);
			}
  	}
  }
  /* USER CODE END SysTick_IRQn 1 */
}

